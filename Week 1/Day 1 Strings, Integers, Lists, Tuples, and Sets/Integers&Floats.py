num = 3
print(type(num))

num = 3.14
print(type(num))

# Arithmetic Operators:
# Addition:       3 + 2
# Subtraction:    3 - 2
# Multiplication: 3 * 2
# Division:       3 / 2
# Floor Division: 3 // 2
# Exponent:       3 ** 2
# Modulus:        3 % 2

print(3 / 2)
print(3 // 2)
print(3 ** 2)
print(3 % 2)

# Incrementing
num = 1
num += 1
print(num)

num *= 10
print(num)

# Absolute value
print(abs(-3))

# Round
print(round(3.75))
print(round(3.75, 1)) # rounds to the first decimal

# Comparison Operators:
# Equal:            3 == 2
# Not Equal:        3 != 2
# Greater Than      3 > 2
# Less than:        3 < 2
# Greater or Equal: 3 >= 2
# Less or Equal:    3 <= 2

num_1 = 3
num_2 = 2

print(num_1 == num_2)
print(num_1 != num_2)
print(num_1 > num_2)
print(num_1 < num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)

num_1 = '100'
num_2 = '200'
print(num_1 + num_2)

# Casting
num_1 = int(num_1)
num_2 = int(num_2)
print(num_1 + num_2)